class User {
  int id;
  String firstName;
  String lastName;
  String username;
  String imageUrl;
  String email;
  String password;

  User({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.username,
    required this.imageUrl,
    required this.email,
    required this.password,
  });
}
